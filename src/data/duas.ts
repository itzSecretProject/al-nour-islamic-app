// Rough difficulty by Arabic length — used to surface shorter duas first for kids.
export function duaDifficulty(arabic: string): 'short' | 'medium' | 'long' {
  const n = arabic.length;
  if (n <= 40) return 'short';
  if (n <= 130) return 'medium';
  return 'long';
}
